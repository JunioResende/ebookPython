#Declaracao das Variaveis"
base = float(input("Digite o valor da base: "))
altura = float(input("Digite o valor da altura: "))

#Variavel criada para calcular Area de um Retangulo"
area = base*altura

#Resultado
print(f"Valor variavel base = {base}\nValor variavel altura {altura}\nArea do Retangulo = {area}")